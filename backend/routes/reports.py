def download_image(url: str, base_url: str = "http://localhost:8000") -> str | None:
    """Download an image from URL and save to temp file. Returns temp file path."""
    if not url:
        return None
    try:
        full_url = url if url.startswith("http") else f"{base_url}{url}"
        response = req.get(full_url, timeout=5)
        if response.status_code == 200:
            ext = url.split(".")[-1].lower()
            if ext not in ["jpg", "jpeg", "png", "gif"]:
                ext = "jpg"
            tmp = tempfile.NamedTemporaryFile(
                delete=False, suffix=f".{ext}"
            )
            tmp.write(response.content)
            tmp.close()
            return tmp.name
    except Exception as e:
        print(f"[PDF] Image download failed: {e}")
    return None


def header_footer(canvas, doc):
    """Draw header and footer on every page."""
    canvas.saveState()
    w, h = A4

    # ── Header bar ────────────────────────────────────────────
    canvas.setFillColor(DARK_BLUE)
    canvas.rect(0, h - 55, w, 55, fill=1, stroke=0)

    # Shield icon (simple polygon)
    canvas.setFillColor(WHITE)
    canvas.setFont("Helvetica-Bold", 18)
    canvas.drawString(20*mm, h - 37, "🛡")

    # Title
    canvas.setFont("Helvetica-Bold", 14)
    canvas.drawString(35*mm, h - 30, "FACEGUARD")
    canvas.setFont("Helvetica", 9)
    canvas.setFillColor(colors.HexColor('#93c5fd'))
    canvas.drawString(35*mm, h - 42, "Surveillance & Communication System")

    # Right side — date
    canvas.setFillColor(WHITE)
    canvas.setFont("Helvetica", 8)
    date_str = datetime.now().strftime("%d %b %Y, %I:%M %p")
    canvas.drawRightString(w - 15*mm, h - 28, f"Generated: {date_str}")
    canvas.drawRightString(w - 15*mm, h - 40, "CONFIDENTIAL — Police Use Only")

    # ── Footer ────────────────────────────────────────────────
    canvas.setFillColor(LIGHT_GRAY)
    canvas.rect(0, 0, w, 20*mm, fill=1, stroke=0)
    canvas.setStrokeColor(BORDER)
    canvas.setLineWidth(0.5)
    canvas.line(15*mm, 20*mm, w - 15*mm, 20*mm)

    canvas.setFillColor(GRAY)
    canvas.setFont("Helvetica", 7.5)
    canvas.drawString(15*mm, 13*mm,
        "This document is generated by FaceGuard — Jaipur Engineering College & Research Centre, Dept. of CSE")
    canvas.drawString(15*mm, 8*mm,
        "CONFIDENTIAL: For authorized police personnel only. Unauthorized use is punishable by law.")
    canvas.drawRightString(w - 15*mm, 10*mm,
        f"Page {doc.page}")

    canvas.restoreState()


